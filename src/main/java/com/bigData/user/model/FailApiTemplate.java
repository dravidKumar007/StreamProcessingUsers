package com.bigData.user.model;

public class FailApiTemplate extends  RestapiTemp{
 int status;
 String error;

    public FailApiTemplate(int status, String error) {
        this.status = status;
        this.error = error;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }
}
